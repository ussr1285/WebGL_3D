def unique_points(triangles):
    # 각 트라이앵글(삼각형)의 포인트를 추출
    triangles = [triangles[i:i+3] for i in range(0, len(triangles), 3)]
    # 2개의 트라이앵글(삼각형)을 합쳐서 사각형을 형성
    squares = [triangles[i] + triangles[i+1] for i in range(0, len(triangles), 2)]

    # 중복을 제거하여 유니크한 포인트만 추출
    unique_squares = []
    for square in squares:
        unique_points = {tuple(point): None for point in square}
        unique_squares.append(list(unique_points.keys()))

    return unique_squares

